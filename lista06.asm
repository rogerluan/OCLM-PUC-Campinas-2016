TITLE EX6 - PAR/IMPAR
.MODEL SMALL
.STACK 100H
.CODE
.DATA
MSG1 DB "DIGITE UM NUMERO:$"
P DB 0AH,0DH,"O NUMERO EH PAR:$"
I DB 0AH,0DH,"O NUMERO EH IMPAR:$"
.CODE 
MOV AX,@DATA
MOV DS,AX
MOV AH,9
LEA DX,MSG1
INT 21H
MOV AH,1
INT 21H
TEST AL,00000001b
JZ PAR
MOV AH,9
LEA DX,I
INT 21H
JMP FIM
PAR:
MOV AH,9
LEA DX,P
INT 21H
FIM:
MOV AH,4CH
INT 21H
END